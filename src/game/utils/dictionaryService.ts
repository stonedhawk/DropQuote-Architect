const fallbackDictionary = new Set([
  'ARC',
  'ART',
  'BAR',
  'BEE',
  'BLOCK',
  'BOARD',
  'BOLT',
  'BRICK',
  'BUILD',
  'CAB',
  'CODE',
  'COLUMN',
  'DROP',
  'GAME',
  'GRID',
  'INK',
  'LAYER',
  'LEVEL',
  'LOCK',
  'MAP',
  'MARIO',
  'MASON',
  'MORTAR',
  'NOTE',
  'ROW',
  'STACK',
  'STEEL',
  'TILE',
  'WORD',
])

export const isValidWord = (word: string) => fallbackDictionary.has(word.toUpperCase())
